##Exemplo para gerar um loço de repetição por meio da tabuada!
## While - enquanto tiver condição para repetir


numero = int(input("Digite o número que deseja a tabuada: "))
i = 1


while i <= 20: ##irá dar sequencia a que foi ordemado
               ## i é o numero que vai ser multiplicado
    print(f"{numero} X {i} = {i*numero}")
    i = i + 1  #Incremento de 1 em 144